public class AssignmentDemo {

    public static void main(String[] args) {

        // assignment operator
        int x =3;
        int y =2;

        x+=y; // x= x+y = 5
        System.out.println("x="+x);

        x-=y; //
        System.out.println("x="+x);

        x*=y; //
        System.out.println("x="+x);

        x/=y; //
        System.out.println("x="+x);

        x%=y; //
        System.out.println("x="+x);
    }


}
